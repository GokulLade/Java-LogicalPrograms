package com.nit.logical.string.day05;


//Write a java program which will print each character of the String separately?
public class PrintingEachCharInStringProgram02 {

	public static void main(String[] args)
	{
		String name="Gokul";
		
		for(int i=0;i<name.length();i++) 
		{
			System.out.print(name.charAt(i)+" ");
			
		}

	}

}
